package Day5;

public class ConcateExample {
    public static void main(String[] args) {

        System.out.println(5 + 4);
        System.out.println(5 + 4 + " hello "); // int + int String
                                               // 9 (int) + String  // String - 9Hello
        System.out.println("Hello" + 5 + 4 ); // Hello54
                                            // String + int + int
                                    // String + int  ---.

    }

}
