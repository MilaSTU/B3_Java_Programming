package Day4_variables;

public class PrintDate {
    public static void main(String[] args) {
        int monthNumber = 3;
        int dayNumber = 28;
        int yearNumber = 2024;
        System.out.println(monthNumber +"/" + dayNumber + "/"+ yearNumber);
        System.out.println(monthNumber +"\\" + dayNumber + "\\"+ yearNumber);
        System.out.println(monthNumber +"-" + dayNumber + "-"+ yearNumber);
    }
}
